function getSlotIdFromUpgradeID(id)
	local ret = false
	for _, arr in pairs(tuningUpgrades) do
		for _, iarr in pairs(arr.upgrades) do
			if (iarr.upgradeID == id) then
				ret = arr.slotID
				break
			end
		end
	end
	return ret
end

